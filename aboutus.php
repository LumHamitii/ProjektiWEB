      The Abington Shoe Company soon moved to Newmarket, New Hampshire, located in a tiny corner of New England known for its majestic mountain ranges, rocky shorelines, dense forests, rivers, lakes, and access to great east coast cities. This part of the world experiences every type of weather—blizzards, instant rainstorms, bitter winters, and summer heat waves. </p> <p style="font-size: 15px;">When we design a new boot, shoe, jacket, pair of glasses, or even a T-shirt, it not only has to look great, we need to consider its impact on nature. Ever since introducing our original waterproof boots, we’ve remained focused on making innovative products that get people outdoors, are made with environmentally-conscious materials, and crafted to last for years. We aim to not only minimize our impact on the environment through the products we make, but to actually help restore the land we depend on.</p><p style="font-size: 15px">We believe in the power of thriving communities to accelerate global change with one unified, passionate voice. And we support the skilled trades as a powerful way to build stronger communities through service. Every full-time Timberland employee gets up to 40 hours of paid time to volunteer in their communities. We’ve served more than 1.2 million hours worldwide, with a goal of reaching 1.5 million hours by 2020.

          </p>
